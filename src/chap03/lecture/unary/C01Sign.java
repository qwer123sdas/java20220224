package chap03.lecture.unary;

public class C01Sign {
	public static void main(String[] args) {
		//부호 연산자
		// +, -
		
		int i = 99;
		System.out.println(+i);  // +99
		System.out.println(-i);  // -99
		
		int j = -88;
		System.out.println(j);  // -88
		System.out.println(-j); // 88
		
		//연산 결과 : int
		short k =77;
//		short l = -k; //오류
	}
}
