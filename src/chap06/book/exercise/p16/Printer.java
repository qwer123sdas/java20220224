package chap06.book.exercise.p16;

public class Printer {
	Printer(){
		
	};
	
	
	void println(int i) {
		System.out.println(i);
	}
	
	void println(boolean b) {
		System.out.println(b);
	}
	
	void println(double d) {
		System.out.println(d);
	}
	
	void println(String s) {
		System.out.println(s);
	}
}
