package chap05.lecture.string;

public class C02String {
	public static void main(String[] args) {
		String s1 = new String("java");
		String s2 = new String("hello");
		String s3 = "java";
		String s4 = "hello";
		
		// 객체 =인스턴스는 자기만의 값과 기능이 있음
		// String클래스에 정의된 메소드와 속성(필드)를 이미 가지고 있어서 사용할 수 있음.
		System.out.println(s1.length());  // 메소드
		System.out.println(s2.length());  // 메소드
		System.out.println(s3.length());  // 메소드
		System.out.println(s4.length());  // 메소드
		
		
		
	}
}
